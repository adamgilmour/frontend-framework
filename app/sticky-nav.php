<script type="text/javascript">
	
	$(document).ready(function(){
		var headerheight = $('#header').outerHeight(true),
	    nav = $('#sticky-nav'),
	    navheight = $('#sticky-nav').outerHeight(true),
	    $this = $(this),
	    id = $this.attr('href'),
	    sections = $('section');

		$("#sticky-nav").sticky({ topSpacing: headerheight});
    
		//scroll down to link
		nav.find('a').on('click', function() {
		var $this = $(this),
		target = $this.attr('href');

		$(target).velocity("scroll", {
		        duration: 1000,
		        offset: -headerheight-navheight,
		        easing: "ease-in-out"
		    });
		return false;
		});

		//add active to section link when visible
		$(window).on('scroll load', function() {
		    var cur_pos = $(this).scrollTop();
		    sections.each(function() {
		        var top = $(this).offset().top - (headerheight + navheight)
		        bottom = top + $(this).outerHeight();
		        if (cur_pos >= top && cur_pos <= bottom) {
		            nav.find('a').removeClass('active');
		            sections.removeClass('active');
		            $(this).addClass('active');
		            nav.find('a[href="#' + $(this).attr('id') + '"]').addClass(
		                'active');
		        }
		    });
		});
	});

</script>

